//STATIC PROPERTIES
Math.E        // returns Euler's number
Math.PI       // returns PI
Math.SQRT2    // returns the square root of 2
Math.SQRT1_2  // returns the square root of 1/2
Math.LN2      // returns the natural logarithm of 2
Math.LN10     // returns the natural logarithm of 10
Math.LOG2E    // returns base 2 logarithm of E
Math.LOG10E   // returns base 10 logarithm of E

//STATIC METHODS
console.log(Math.ceil(4.76))	//Returns x rounded up to its nearest integer
console.log(Math.round(4.76))	//Returns x rounded to its nearest integer
console.log(Math.floor(4.76))	//Returns x rounded down to its nearest integer
console.log(Math.trunc(4.76))	//Returns the integer part of x (new in ES6)
